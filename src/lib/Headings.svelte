<section id="headings">
    <h2>Headings</h2>
    <p>
        <span class="HTML">HTML</span> headings are used to define the structure of a webpage, helping both users and search engines understand the content hierarchy.
      There are six levels of headings, from <code>&lt;h1&gt;</code> to <code>&lt;h6&gt;</code>, with <code>&lt;h1&gt;</code> being the most important.
    </p>
  
    <div class="code-block">
<pre><code>    &lt;h1&gt;Main Title&lt;/h1&gt; 
    &lt;h2&gt;Section Heading&lt;/h2&gt;
    &lt;h3&gt;Subsection Heading&lt;/h3&gt;
    &lt;h4&gt;Minor Heading&lt;/h4&gt;
    &lt;h5&gt;Smaller Heading&lt;/h5&gt;
    &lt;h6&gt;Least Important Heading&lt;/h6&gt;</code></pre>
    </div>
  
    <p>
      Use headings in order without skipping levels to maintain accessibility and SEO best practices. A good rule of thumb is to only use one <code>&lt;h1&gt;</code> per page, typically for the main title.
    </p>
